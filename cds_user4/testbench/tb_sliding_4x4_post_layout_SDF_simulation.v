`timescale 1ns / 1ps

module tb_sliding_4x4_post_layout_SDF_simulation;

    // ============================================================
    // DUT Inputs
    // ============================================================

    reg clk;
    reg rst;
    reg enable;

    reg signed [3:0] a;
    reg signed [3:0] b;
    
    wire a0,a1,a2,a3,b0,b1,b2,b3;
    wire signed [3:0] anew,bnew;
    // ============================================================
    // DUT Outputs
    // ============================================================

    wire signed [15:0] mac_out;

    integer out_count;

    // ============================================================
    // 5 MHz CLOCK (200 ns PERIOD)
    // ============================================================

    initial begin
        clk = 1'b0;
        forever #100 clk = ~clk;
    end

    // ============================================================
    // DUT
    // ============================================================

    mac_4x4_wrapper_chip DUT (
        .clk     (clk),
        .rst     (rst),
        .enable  (enable),
        .a       (anew),
        .b       (bnew),
        .mac_out (mac_out)
    );
	assign anew={a3,a2,a1,a0};
	assign bnew={b3,b2,b1,b0};
	assign a0=a[0];
	assign a1=a[1];
	assign a2=a[2];
	assign a3=a[3];

	assign b0=b[0];
	assign b1=b[1];
	assign b2=b[2];
	assign b3=b[3];


    // ============================================================
    // MAIN STIMULUS
    // ============================================================

    initial begin

        out_count = 0;

        //--------------------------------------------------------
        // Initialize
        //--------------------------------------------------------

        rst    = 1'b1;
        enable = 1'b0;
        a      = 4'sd0;
        b      = 4'sd0;

        //--------------------------------------------------------
        // RESET SETTLING
        //--------------------------------------------------------

        repeat (5) @(posedge clk);

        rst = 1'b0;

        repeat (5) @(posedge clk);

        //--------------------------------------------------------
        // START MAC OPERATIONS
        //--------------------------------------------------------

        @(negedge clk);
        enable <= 1'b1;

        //--------------------------------------------------------
        // +3 × +2 = +6
        //--------------------------------------------------------

        @(negedge clk);
        a <= 4'sd3;
        b <= 4'sd2;

        //--------------------------------------------------------
        // +4 × +5 = +20
        //--------------------------------------------------------

        @(negedge clk);
        a <= 4'sd4;
        b <= 4'sd5;

        //--------------------------------------------------------
        // -2 × +3 = -6
        //--------------------------------------------------------

        @(negedge clk);
        a <= -4'sd2;
        b <=  4'sd3;

        //--------------------------------------------------------
        // -4 × -2 = +8
        //--------------------------------------------------------

        @(negedge clk);
        a <= -4'sd4;
        b <= -4'sd2;

        //--------------------------------------------------------
        // +7 × -1 = -7
        //--------------------------------------------------------

        @(negedge clk);
        a <=  4'sd7;
        b <= -4'sd1;

        //--------------------------------------------------------
        // STOP INPUTS CLEANLY
        //--------------------------------------------------------

        @(negedge clk);
        enable <= 1'b0;
        a <= 4'sd0;
        b <= 4'sd0;

        //--------------------------------------------------------
        // PIPELINE DRAIN
        //--------------------------------------------------------

        repeat (50) @(posedge clk);

        $display("====================================================");
        $display("TOTAL OBSERVATION CYCLES : %0d", out_count);
        $display("====================================================");

        $finish;

    end

    // ============================================================
    // OUTPUT MONITOR
    // ============================================================

    initial begin

        $display("==============================================================");
        $display(" Time(ns)\trst\tenable\ta\tb\tmac_out");
        $display("==============================================================");

        forever @(posedge clk) begin

            out_count = out_count + 1;

            $display(
                "%0t\t%b\t%b\t%0d\t%0d\t%0d",
                $time,
                rst,
                enable,
                a,
                b,
                mac_out
            );

        end

    end

    // ============================================================
    // OPTIONAL CONTINUOUS MONITOR
    // ============================================================

    always @(posedge clk) begin

        $display(
            "T=%0t rst=%b en=%b a=%0d b=%0d mac_out=%0d",
            $time,
            rst,
            enable,
            a,
            b,
            mac_out
        );

    end

    // ============================================================
    // SDF ANNOTATION
    // Replace path with generated SDF file
    // ============================================================

    initial begin

        $sdf_annotate(
            "../4_Physical_Design_Flow3_6M1L_16/5_Routing/Routing/mac_4x4_wrapper_chip_postRoute_with_pad_delay.sdf",
            DUT,
            ,
            "sdf.log"
        );

    end

    // ============================================================
    // DUMP WAVES
    // ============================================================

    initial begin
        $dumpfile("mac_4x4_wrapper_chip_post_synthesis.vcd");
        $dumpvars(0, tb_sliding_4x4_post_layout_SDF_simulation);
    end

endmodule
