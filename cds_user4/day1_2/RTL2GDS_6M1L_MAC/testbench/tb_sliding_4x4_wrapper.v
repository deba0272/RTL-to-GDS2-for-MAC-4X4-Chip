`timescale 1ns / 1ps

module tb_mac_4x4_wrapper_chip;

    //--------------------------------------------------
    // DUT Inputs
    //--------------------------------------------------

    reg clk;
    reg rst;
    reg enable;

    reg signed [3:0] a;
    reg signed [3:0] b;

    //--------------------------------------------------
    // DUT Outputs
    //--------------------------------------------------

    wire signed [15:0] mac_out;

    //--------------------------------------------------
    // Instantiate DUT
    //--------------------------------------------------

    mac_4x4_wrapper_chip dut (
        .clk(clk),
        .rst(rst),
        .enable(enable),
        .a(a),
        .b(b),
        .mac_out(mac_out)
    );

    //--------------------------------------------------
    // Clock Generation
    //--------------------------------------------------

    initial
    begin
        clk = 1'b0;
        forever #5 clk = ~clk;   // 100 MHz
    end

    //--------------------------------------------------
    // Stimulus
    //--------------------------------------------------

    initial
    begin

        //--------------------------------------------------
        // Initialize
        //--------------------------------------------------

        rst    = 1'b1;
        enable = 1'b0;
        a      = 4'sd0;
        b      = 4'sd0;

        #30;

        //--------------------------------------------------
        // Release Reset
        //--------------------------------------------------

        rst = 1'b0;

        @(posedge clk);

        //--------------------------------------------------
        // MAC Operations
        //--------------------------------------------------

        enable = 1'b1;

        // +3 * +2 = +6
        a = 4'sd3;
        b = 4'sd2;
        @(posedge clk);

        // +4 * +5 = +20
        a = 4'sd4;
        b = 4'sd5;
        @(posedge clk);

        // -2 * +3 = -6
        a = -4'sd2;
        b =  4'sd3;
        @(posedge clk);

        // -4 * -2 = +8
        a = -4'sd4;
        b = -4'sd2;
        @(posedge clk);

        // +7 * -1 = -7
        a =  4'sd7;
        b = -4'sd1;
        @(posedge clk);

        //--------------------------------------------------
        // Stop Feeding Inputs
        //--------------------------------------------------

        enable = 1'b0;
        a = 0;
        b = 0;

        repeat(10) @(posedge clk);

        $finish;

    end

    //--------------------------------------------------
    // Monitor
    //--------------------------------------------------

    initial
    begin
        $display("----------------------------------------------------------");
        $display(" Time    rst en     a     b      mac_out");
        $display("----------------------------------------------------------");

        $monitor("%5t     %b   %b    %4d  %4d     %6d",
                  $time,
                  rst,
                  enable,
                  a,
                  b,
                  mac_out);
    end

    //--------------------------------------------------
    // Dump Waves
    //--------------------------------------------------

    initial
    begin
        $dumpfile("mac_4x4_wrapper_chip.vcd");
        $dumpvars(0, tb_mac_4x4_wrapper_chip);
    end

endmodule
